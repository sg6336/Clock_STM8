#include <stdint.h>

// ============================================================================
//     Public API functions
// ============================================================================
void delay_ms(uint16_t ms)
{
    volatile uint16_t i;

    while (ms--)
    {
        for (i = 0; i < 1600; i++)
        {
            __asm("nop");
        }
    }
}